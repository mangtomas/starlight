   protected $content;
   protected $status;
   
   
   public function __construct($content = '', $status = 200)
   {
      $this->content($content)->status($status);
   }
   
   public function content($content)
   {
      $this->content = $content;
      
      return $this;
   }
   
   public function status($status)
   {
      $this->status = $status;
      
      return $this;
   }
};
